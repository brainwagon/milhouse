void
verifythreemoves()
{
    int i, n, correct=0 ;
    n = sizeof(opening) / sizeof(opening[0]) ;
    for (i=0; i<n; i++) {
	if (opening[i].pos.hash != recomputeboardhash(&opening[i].pos, COLOR_WHITE))
	    fprintf(stderr, "opening %d didn't match\n", i) ;
	else
	    correct ++ ;
    }
    fprintf(stderr, "%d of %d openings verified.\n", correct, n) ;
}

